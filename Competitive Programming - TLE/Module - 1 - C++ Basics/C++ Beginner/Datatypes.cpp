#include<bits/stdc++.h>
using namespace std;

int main()
{

    // Integer types
    int a = 10;   // Regular integer
    long long int b = 1000000;   // Large integer
    unsigned int c = 42;   // Non - negative integer

    // Character type
    char d = 'A';   // Single Character

    // Boolean type
    bool e = true;   // Boolean(true/false)

    // Floating - point types
    float f = 3.14f;   // Single - precision floating - point
    double g = 3.1415926535;   // Double - precision floating - point
    long double n = 3.141592653589793238L;   // Extended - precision floating - point

    // Special type
    void* ptr = nullptr;   // Void pointer(points to no type)

    return 0;
}